class Ascii
{
public static void main(String[] args)
	{
	int i=81;
	char c= (char)i;
	System.out.println("i= "+ i);
	System.out.println("c="+ c);
	double d=323.142;
	i= (int)d;
	System.out.println("d="+ d);
	System.out.println("i= "+ i);
	double f=72.38;
	i= (int)f;
	System.out.println("f="+ f);
	System.out.println("i= "+ i);
	int b=(byte)d;
	System.out.println("d="+ d);
	System.out.println("b="+b);
	}
}